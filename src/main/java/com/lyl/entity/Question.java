package com.lyl.entity;

import java.util.Date;

public class Question {

    private String id;

    private String title;

    private String tag;

    private String content;

    private int likeCount;

    private int answerCount;

    private int followCount;

    private int browseCount;

    private Date createDate;

    private String createBy;

    private Date lastRespondDate;

    private String lastRespondBy;

    private String createUserName;

    public String getCreateUserName() {
        return createUserName;
    }

    public void setCreateUserName(String createUserName) {
        this.createUserName = createUserName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getLikeCount() {
        return likeCount;
    }

    public void setLikeCount(int likeCount) {
        this.likeCount = likeCount;
    }

    public int getFollowCount() {
        return followCount;
    }

    public void setFollowCount(int followCount) {
        this.followCount = followCount;
    }

    public int getBrowseCount() {
        return browseCount;
    }

    public void setBrowseCount(int browseCount) {
        this.browseCount = browseCount;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    public String getCreateBy() {
        return createBy;
    }

    public void setCreateBy(String createBy) {
        this.createBy = createBy;
    }

    public Date getLastRespondDate() {
        return lastRespondDate;
    }

    public void setLastRespondDate(Date lastRespondDate) {
        this.lastRespondDate = lastRespondDate;
    }

    public String getLastRespondBy() {
        return lastRespondBy;
    }

    public void setLastRespondBy(String lastRespondBy) {
        this.lastRespondBy = lastRespondBy;
    }

    public int getAnswerCount() {
        return answerCount;
    }

    public void setAnswerCount(int answerCount) {
        this.answerCount = answerCount;
    }
}
